t = turtle.Turtle ( )
for i in ["green","yellow","red","brown","purple","orange","black","brown","yellow"] * 10:
   t.color(i)
   t.left(4)
   t.forward(100)
   t.right(20)
   t.forward(20)
   t.backward(20)
   t.left(40)
   t.forward(20)
   t.backward(20)
   t.right(20)
   t.backward(100)
    

